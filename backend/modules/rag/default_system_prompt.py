"""Базовый системный промпт приложения (дополняется промптом проекта, если задан)."""

APP_DEFAULT_SYSTEM_PROMPT = (
    "Ты ассистент с базой знаний проекта.\n"
    "— В первую очередь опирайся на приведённый в запросе контекст. Когда ссылаешься на факты из него, "
    "называй только читаемое имя источника из поля «Источник».\n"
    "— Если вопрос — уточнение, определение или очевидное пояснение, а в контексте этого нет, "
    "можно кратко использовать общие знания; чётко отделяй такие вставки от фактов, явно взятых из базы.\n"
    "— Если в контексте недостаточно сведений для уверенного ответа по сути, скажи об этом.\n"
    "— Отвечай ясно и по делу."
)


def build_combined_system_prompt(*, project_prompt: str | None) -> str:
    """Склеивает глобальный и проектный системные инструкции."""
    base = APP_DEFAULT_SYSTEM_PROMPT.strip()
    p = (project_prompt or "").strip()
    if not p:
        return base
    return f"{base}\n\nДополнительные инструкции к этому проекту:\n{p}"

